<h2>Nuestra Historia</h2>
<br />
<p>
	Draw This comenzó como un proyecto de nuestra Media Técnica en el aula, una idea audaz que tomó
	forma a medida que explorábamos el fascinante mundo del arte. Lo que comenzó como una simple
	conversación en el campus de la universidad se convirtió rápidamente en un viaje de
	autodescubrimiento y creatividad sin límites. Nos dimos cuenta de que compartíamos una pasión
	común por el arte en todas sus formas: desde la pintura y el dibujo hasta la ilustración digital y
	la escultura.
</p>
<br />
<h2>Nuestra Misión</h2>
<br />
<p>
